public class Tc4 {
    public static void main(String[] args) {
        float num = 7.65f;
        short result = (short) num;  // Explicit casting from float to short
        
        System.out.println("Explicit Casting (float to short):");
        System.out.println("Float value: " + num);
        System.out.println("Short value (after casting): " + result);
    }
}
