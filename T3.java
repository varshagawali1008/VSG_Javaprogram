public class T3 {
    public static void main(String[] args) {
        byte smallNum = 10;
        int largeNum = smallNum;  // Implicit casting from byte to int
        
        System.out.println("Implicit Casting (byte to int):");
        System.out.println("Byte value: " + smallNum);
        System.out.println("Integer value: " + largeNum);
    }
}

