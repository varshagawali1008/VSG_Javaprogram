public class Tc2 {
    public static void main(String[] args) {
        double num = 123.45;
        int result = (int) num;  // Explicit casting from double to int
        
        System.out.println("Explicit Casting (double to int):");
        System.out.println("Double value: " + num);
        System.out.println("Integer value (after casting): " + result);
    }
}

